    private void dodaj_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dodaj_buttonActionPerformed
        String camera,camera_path;
        camera = JOptionPane.showInputDialog(this,"Podaj nazwe aparatu");
        if(!camera.equals("")){
            JFileChooser myFileChooser = new JFileChooser();
            myFileChooser.setDialogTitle("Wybierz sciezke dla: "+camera);
            myFileChooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);

            if(myFileChooser.showOpenDialog(null) == JFileChooser.APPROVE_OPTION){
                String path = myFileChooser.getSelectedFile().getAbsolutePath();
                camera_path = path;
                
                try {
                    act.configuration_file.dodaj("%presets", camera+" "+camera_path);
                } catch (FileNotFoundException ex) {
                    Logger.getLogger(zarzadzaj_presets_window.class.getName()).log(Level.SEVERE, null, ex);
                }
                act = new Configuration();
                reload();
            }
            else{
                JOptionPane.showMessageDialog(this, "Nie wybrales pliku");
            }
            }
        else{
            JOptionPane.showMessageDialog(this, "Pole puste");
        }
    }//GEN-LAST:event_dodaj_buttonActionPerformed

    private void usun_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_usun_buttonActionPerformed
        if(!selected.equals("")){
            try {
                act.configuration_file.usun_wartosc("%presets", selected);
                reload();
            } catch (FileNotFoundException ex) {
                Logger.getLogger(zarzadzaj_presets_window.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else{
            JOptionPane.showMessageDialog(this, "Nic nie zosta≈Ço zaznaczone");
        }
    }//GEN-LAST:event_usun_buttonActionPerformed

